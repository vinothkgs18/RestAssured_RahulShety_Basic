package E_Serialization_Pojo;

public class Location {
	private double lat;
	private double lng;
	/**
	 * @return the lat
	 */
	public double getLat() {
		return lat;
	}
	/**
	 * @return the lng
	 */
	public double getLng() {
		return lng;
	}
	/**
	 * @param lat the lat to set
	 */
	public void setLat(double lat) {
		this.lat = lat;
	}
	/**
	 * @param lng the lng to set
	 */
	public void setLng(double lng) {
		this.lng = lng;
	}
	
	

}
